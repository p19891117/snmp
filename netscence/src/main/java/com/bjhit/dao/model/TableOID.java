package com.bjhit.dao.model;

import java.util.List;

public class TableOID {
	private Integer id ;
	private String oid;
	private String flag;
	private String name;
	private List<TableOIDSub> tableOidSubs;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getOid() {
		return oid;
	}
	public void setOid(String oid) {
		this.oid = oid;
	}
	public List<TableOIDSub> getTableOidSubs() {
		return tableOidSubs;
	}
	public void setTableOidSubs(List<TableOIDSub> tableOidSubs) {
		this.tableOidSubs = tableOidSubs;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
